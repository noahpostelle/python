import turtle 
  

tortle = turtle.Screen()
  
tortle.title("turtle")
tortle = turtle.Turtle()
for i in range(4):
    tortle.forward(100)
    tortle.left(90)
tortle.goto(50,50)
for i in range(4):
    tortle.forward(100)
    tortle.left(90)
tortle.goto(150,50)
tortle.goto(100,0)
tortle.goto(100,100)
tortle.goto(150,150)
tortle.goto(50,150)
tortle.goto(0,100)
turtle.done()